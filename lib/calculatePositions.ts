    if (
        (movement === null) ||
        (cursor.y === 0 && movement === "down") ||
        (cursor.y === canvaSize.height + 1 && movement === "up") ||
        (cursor.x === 0 && movement === "left") ||
        (cursor.x === canvaSize.width + 1 && movement === "right")    
    ){ 
        console.log(cursor, movement, "🟥");

        return {
            newCanva: null,
            newCursor: null
        }   
    }
    
