// Initialize the canva and cursor
export function initializeCanva(
    canvaHeight: number, 
    canvaWidth: number, 
    level: number
){

    canvaSize.width = canvaWidth;
    canvaSize.height = canvaHeight;
    const colorNumber = Math.floor(level / 10) + 4;

    // Setup Canva, fill with 0
    let initialCanva: Canva = Array(canvaHeight).fill(0).map(() => Array(canvaWidth).fill(null))
    // Setup Canva, fill with correct values
    initialCanva = initialCanva.map((row, y) => (row.map((cell, x) => (Math.ceil(Math.random() * colorNumber)))))
    // Setup Cursor
    const initialCursor: Cursor = { x: 0, y: 0, value: Math.ceil(Math.random() * colorNumber) };
    return {
        initialCanva,
        initialCursor
    }
}
